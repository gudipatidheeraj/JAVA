import java.util.*;
public class typecasting {
    public static void main(String[] args) {
        Scanner inp = new Scanner(System.in);
        float curr_age = inp.nextFloat();
        int age = (int)curr_age;
        System.out.println("Current age is: " +curr_age);
        System.out.println("Exact age is: " + age);
    }
}
//likewise you can typecast a variable type to one another...

